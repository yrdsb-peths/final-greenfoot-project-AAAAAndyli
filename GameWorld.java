    private void buildWorld(int[][] world)
    {
        for(int j = 0; j < 12; j++)
        {
            for(int i = 0; i < 24; i++)
            {
                if(world[j][i] == 1)
                {
                    Box box = new Box();
                    addObject(box,i*50+25,j*50+25);
                }
                else if(world[j][i] == 2)
                {
                    addObject(player,i*50+25,j*50+25);
                    playerSpawnX = i*50+25;
                    playerSpawnY = j*50+25;
                }
                else if(world[j][i] == 3)
                {
                    Enemy enemy = new Enemy();
                    addObject(enemy,i*50+25,j*50+26);
                }
                else if(world[j][i] == 4)
                {
                    AEnemy advancedenemy = new AEnemy();
                    addObject(advancedenemy,i*50+25,j*50+39);
                }
                else if(world[j][i] == 5)
                {
                    End end = new End();
                    addObject(end,i*50+25,j*50+26);
                }
            }
        }
    }
    
